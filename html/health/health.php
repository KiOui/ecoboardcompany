                    <h5>Will the emissions to air increase when I use MDI as a binder?</h5>
                    <br>
                    Emissions to air of MDI are generally within regulatory emission limits. In addition, due to the fact that MDI also chemically bonds with amines, alcohols and other functional groups, the total volatile organic emissions tend to decrease when MDI is used as a resin.
                    <br><br>
                    <h5>Is MDI emitted from wood panels, which could lead to indoor air issues?</h5>
                    <br>
                    Several studies carried out by independent organizations demonstrated that emissions of MDI bonded wood panels are negligible and in most cases not detectable. MDI resin from ECOBoard is not an additional source contributing to indoor air formaldehyde emissions from composite wood panels.
                    <br><br>
                    <h5>Do panels bonded with MDI resin cause an increase in emissions of toxic products when combusted?</h5>
                    <br>
                    An independent study has demonstrated that panels bonded with MDI resin do not emit significant levels of toxic compounds compared to conventionally bonded panels.
